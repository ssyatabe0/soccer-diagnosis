// 共通テンプレート
function getCommonTemplate(typeName: string): TypeTemplate {
  return {
    title: typeName,
    cause: `「${typeName}」タイプのお子さんは、技術や練習量の問題ではなく、才能の"出し方"がまだ最適化されていないだけです。`,
    points: ['試合での動き出し', 'ボールを受ける前の準備', '自分の強みを出すタイミング'],
    mistakes: ['練習量だけ増やす', '性格やメンタルのせいにする', '原因を特定せず闇雲に練習する'],
    nextStep: 'まずは試合中のどの場面で止まっているかを特定するのが近道です。映像があると具体的に整理できます。',
  };
}

export function getTemplate(typeName: string): TypeTemplate {
  return TEMPLATES[typeName] || getCommonTemplate(typeName);
}

