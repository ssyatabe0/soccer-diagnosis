// 共通テンプレート（未対応タイプ用）
function getCommonTemplate(typeName: string): Template {
  return {
    cause: `「${typeName}」タイプのお子さんは、技術や練習量の問題ではなく、才能の"出し方"がまだ最適化されていないだけです。`,
    points: '・試合での動き出し\n・ボールを受ける前の準備\n・自分の強みを出すタイミング',
    mistake: '・練習量だけ増やす\n・性格やメンタルのせいにする\n・原因を特定せず闇雲に練習する',
    next: 'まずは試合中のどの場面で止まっているかを特定するのが近道です。映像があると具体的に整理できます。',
  };
}

export function buildResultMessages(typeId: number): string[] {
  const type = DIAGNOSIS_TYPES.find(t => t.id === typeId) || DIAGNOSIS_TYPES[0];
  const tmpl = TEMPLATES[typeId] || getCommonTemplate(type.name);

  const msg1 = `【${type.name}】の続きの結果をお届けします。

🔍 原因の再定義

${tmpl.cause}`;

  const msg2 = `🌱 伸びるポイント

${tmpl.points}

⚠ よくある間違い

${tmpl.mistake}`;

  const msg3 = `🎯 次の一手

${tmpl.next}

▼ オンライン診断（動画で診てもらう）
${ONLINE_URL}

▼ 対面で直接見てもらう（優先予約）
${VISIT_URL}`;

  return [msg1, msg2, msg3];
}
